		  STORE INDATA

SHIFTNOW: LOAD INDATA
		  OUT   LEDS
		  OUT SEVENSEG
		  SHIFT 1
		  STORE INDATA
		  OUT TIMER

TIME:	  IN TIMER
		  ADDI -20
		  JPOS SHIFTNOW
		  JUMP TIME

